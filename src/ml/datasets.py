"""Supervised dataset construction for ML research.

SupervisedDataset is the central exchange object between the feature/label
generation layer (E0) and future predictive models.

Design properties:
- Frozen dataclass: field reassignment is prevented.
- No timestamps or provenance: those belong in experiment artefacts.
- No persistence: in-memory only; reconstruct from prices when needed.
- slice() is the only method: it exists for walk-forward loop compatibility.
  Splits are always generated by src.validation, not by this class.
"""

from __future__ import annotations

import hashlib
from dataclasses import dataclass

import pandas as pd

from src.ml.feature_matrix import align_features_and_labels


@dataclass(frozen=True)
class SupervisedDataset:
    """An aligned, leakage-safe (X, y) pair ready for model consumption.

    All fields are immutable (frozen dataclass).  The DataFrame and Series
    fields cannot be reassigned, though their contents remain mutable as is
    standard for pandas objects.

    horizon must match the shift(-horizon) used to generate y.  It is stored
    here as an auditable record of the lookahead assumption — future model
    code and provenance tooling can read it.

    feature_names is a tuple of the X column names at construction time.
    It exists as a cheap structural record separate from X.columns, which
    is useful for hashing and provenance without touching the data.

    Do NOT add created_at or any runtime timestamp here.  If provenance is
    needed, record it in the experiment artefact metadata sidecar.
    """

    X: pd.DataFrame                 # (n_obs, n_features), DatetimeIndex
    y: pd.Series | pd.DataFrame     # (n_obs,) or (n_obs, n_targets), same index
    feature_names: tuple[str, ...]
    label_name: str
    horizon: int                    # look-ahead in periods (trading days)

    def slice(
        self,
        start: str | pd.Timestamp,
        end: str | pd.Timestamp,
    ) -> SupervisedDataset:
        """Return a new dataset restricted to the closed interval [start, end].

        Intended for use inside walk-forward loops.  Pass TimeSplit boundaries
        from src.validation.splits directly.

        Splits are owned by the validation layer — this method only slices;
        it does not generate, validate, or reason about split design.

        Args:
            start: Start date string (e.g. "2018-01-01") or Timestamp.
            end: End date string or Timestamp (inclusive).

        Returns:
            New SupervisedDataset with identical metadata and sliced data.
        """
        return SupervisedDataset(
            X=self.X.loc[start:end],
            y=self.y.loc[start:end],
            feature_names=self.feature_names,
            label_name=self.label_name,
            horizon=self.horizon,
        )


def build_supervised_dataset(
    X: pd.DataFrame,
    y: pd.Series | pd.DataFrame,
    horizon: int,
    label_name: str | None = None,
) -> SupervisedDataset:
    """Align X and y, drop NaN rows, and return a SupervisedDataset.

    This is the canonical constructor.  It calls align_features_and_labels()
    internally; callers should pass raw (potentially NaN-containing) feature
    and label arrays.

    label_name resolution order:
    1. Explicit label_name argument.
    2. y.name (if y is a named pd.Series).
    3. "target" (fallback).

    Args:
        X: Feature matrix (DatetimeIndex × features), may contain NaN.
        y: Label series or DataFrame with forward-shifted targets, may contain
            NaN on the last horizon rows.
        horizon: The shift(-horizon) used to generate y.  Stored as metadata.
        label_name: Optional override for the label name.

    Returns:
        SupervisedDataset with aligned, NaN-free (X, y) and horizon metadata.
    """
    X_aligned, y_aligned = align_features_and_labels(X, y)

    if label_name is not None:
        resolved_name = label_name
    elif isinstance(y, pd.Series) and y.name is not None:
        resolved_name = str(y.name)
    else:
        resolved_name = "target"

    return SupervisedDataset(
        X=X_aligned,
        y=y_aligned,
        feature_names=tuple(str(c) for c in X_aligned.columns),
        label_name=resolved_name,
        horizon=horizon,
    )


def dataset_hash(dataset: SupervisedDataset) -> str:
    """Lightweight deterministic hash of dataset metadata.

    Hashes structural metadata only — not data values.  This matches the
    philosophy of experiment_hash() in src/experiments/config.py.

    Hash inputs (in order):
        feature_names joined by pipe, label_name, horizon,
        index start date, index end date, X shape, y shape.

    Callers can detect dataset identity changes (different features, different
    horizon, different date range, different shape) without reading data values.

    Args:
        dataset: The SupervisedDataset to hash.

    Returns:
        12-character lowercase hexadecimal string.
    """
    if len(dataset.X) == 0:
        index_range = "empty"
    else:
        index_range = f"{dataset.X.index[0]}|{dataset.X.index[-1]}"

    parts = [
        "|".join(dataset.feature_names),
        dataset.label_name,
        str(dataset.horizon),
        index_range,
        str(dataset.X.shape),
        str(dataset.y.shape),
    ]
    digest = hashlib.sha256("|".join(parts).encode()).hexdigest()
    return digest[:12]
